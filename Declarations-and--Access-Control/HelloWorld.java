package basic.java;

//simplest program to start off

public class HelloWorld {
		
	public static void main(String[] args) {
		/*There will be only one main() which will be call by JVM and 
		 * if we overrride main method then we have to call it ourself*/
		/*main() access modifier is public so it can be access from anywhere, 
		 * static will load this method into memory at first and 
		 * void will be responsible for returning nothing.*/
		System.out.println("hello world");
	}

}
